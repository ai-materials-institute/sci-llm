"""Filter and classify property candidates from extracted properties CSV files.

This script processes CSV files generated by mass_extract_properties_from_llm.py
from the unsupervised_llm_extraction directory and adds a "data_type" column that
classifies the value_string as:

- "numerical": Single numeric values (with or without units)
  Examples: "25.1 mPa·S", "~31.6 mN·m-1", "around 12 mN·m-1"

- "numerical_range": Numeric ranges or uncertainties
  Examples: "1.54±0.04 to 2.43±0.18", "2.06 ± 0.08", "> 2", "5 ~ 10"

- "string": Non-numeric text descriptions
  Examples: "good emulsifying ability", "rapidly adsorbing"

The script reads CSV files from: <output_dir>/unsupervised_llm_extraction/
And writes a single combined CSV file to: <output_dir>/candidates/extracted_properties_combined.csv

Usage (from within an examples subdirectory):
```bash
# Example: from examples/biosurfactants-extraction/
cd examples/biosurfactants-extraction

# Process with specific output directory
uv run pbench-filter -od OUTPUT_DIR
```
"""

import argparse
import logging
import random
import re
from pathlib import Path

import pandas as pd

import pbench

logger = logging.getLogger(__name__)


def is_numeric(value_str: str) -> bool:
    """Check if a string represents a numeric value.

    Args:
        value_str: String to check

    Returns:
        True if the string is numeric, False otherwise

    """
    if not value_str or not isinstance(value_str, str):
        return False

    # Clean the string - remove common units and special characters
    # but keep numbers, decimal points, signs, and scientific notation
    cleaned = re.sub(r"[^\d\.\-\+eE]", "", value_str.strip())

    if not cleaned:
        return False

    try:
        float(cleaned)
        return True
    except ValueError:
        return False


def classify_value_string(value_str: str) -> str:
    """Classify a value_string as string, numerical, or numerical_range.

    Uses the logic from src/pbench/utils.py to detect ranges.

    Args:
        value_str: The value string to classify

    Returns:
        One of: "string", "numerical", "numerical_range"

    """
    if pd.isna(value_str) or not value_str or not isinstance(value_str, str):
        return "string"

    value_str = str(value_str).strip()

    # Check for range indicators (from utils.py lines 33 and 40-43)
    # More specific patterns to avoid false positives

    # Check for explicit range patterns with numbers on both sides
    # Pattern: number [range_indicator] number
    range_patterns = [
        r"\d+\.?\d*\s*[–~]\s*\d+\.?\d*",  # e.g., "1.5–2.5" (en-dash), "5 ~ 10" (tilde)
        r"\d+\.?\d*\s+to\s+\d+\.?\d*",  # e.g., "1.5 to 2.5"
        r">\s*\d+\.?\d*",  # e.g., "> 2"
        r"<\s*\d+\.?\d*",  # e.g., "< 2"
        r"\d+\.?\d*\s*±\s*\d+\.?\d*",  # e.g., "1.5±0.1" (plus-minus with two numbers)
        r"from\s+.*\s+to\s+.*\d",  # e.g., "from > 2 to ~1.7"
    ]

    for pattern in range_patterns:
        if re.search(pattern, value_str, re.IGNORECASE):
            return "numerical_range"

    # Check if it's a pure number
    if is_numeric(value_str):
        return "numerical"

    # Check if it contains numbers (might be a number with units)
    # If it has digits and looks like a measurement, classify as numerical
    if re.search(r"\d", value_str):
        # Look for patterns like "~31.6", "around 12", "approximately 5.5"
        # These are approximate single values, not ranges
        approx_pattern = r"(?:~|around|approximately|about|ca\.?)\s*\d+\.?\d*"
        if re.search(approx_pattern, value_str, re.IGNORECASE):
            return "numerical"

        # Extract just the numeric part at the beginning
        numeric_part = re.match(r"^\s*[-+]?[\d]+\.?[\d]*(?:[eE][-+]?\d+)?", value_str)
        if numeric_part:
            # It starts with a number, likely a measurement with units
            return "numerical"

    return "string"


def process_csv_file(input_path: Path) -> pd.DataFrame | None:
    """Process a single CSV file and add data_type column.

    Args:
        input_path: Path to input CSV file

    Returns:
        DataFrame with data_type column added, or None if processing failed

    """
    logger.info(f"Processing {input_path.name}")

    # Read CSV
    # refno on arxiv IDs can be coerced to float, we don't want that
    # e.g. 08123.1 should stay "08123.1", it should not become 8123.1
    df = pd.read_csv(input_path, dtype={"refno": "string"})

    # Check if value_string column exists
    if "value_string" not in df.columns:
        logger.warning(f"No 'value_string' column found in {input_path.name}, skipping")
        return None

    # Check if data_type column already exists
    if "data_type" in df.columns:
        logger.warning(
            f"'data_type' column already exists in {input_path.name}, will overwrite it"
        )

    # Add data_type column
    df["data_type"] = df["value_string"].apply(classify_value_string)

    # Log statistics
    type_counts = df["data_type"].value_counts()
    logger.info(f"Data type distribution: {type_counts.to_dict()}")

    return df


def insert_placeholder_rows(
    df: pd.DataFrame, target_properties: list[str]
) -> pd.DataFrame:
    """Insert blank rows so every material has at least one row per target property.

    Placeholders are inserted right after each (material, refno) group's existing rows.

    Args:
        df: DataFrame with 'material_or_system', 'property_name', and 'refno' columns
        target_properties: List of property names that every material should have

    Returns:
        DataFrame with placeholder rows inserted in-place for missing properties

    """
    # Pre-compute existing properties per (material, refno)
    existing_props: dict[tuple[str, str], set[str]] = {}
    for (material, refno), group in df.groupby(["material_or_system", "refno"]):
        existing_props[(material, refno)] = set(group["property_name"].dropna())

    # Find the last row index for each (material, refno) group
    rows: list[dict] = df.to_dict("records")
    last_idx_per_group: dict[tuple[str, str], int] = {}
    for i, row in enumerate(rows):
        key = (row["material_or_system"], str(row["refno"]))
        last_idx_per_group[key] = i

    # Insert placeholders after the last row of each (material, refno) group
    result_rows: list[dict] = []
    n_placeholders = 0
    for i, row in enumerate(rows):
        result_rows.append(row)
        key = (row["material_or_system"], str(row["refno"]))
        if i == last_idx_per_group[key]:
            missing = [
                p for p in target_properties if p not in existing_props.get(key, set())
            ]
            for prop in missing:
                result_rows.append(
                    {
                        "material_or_system": row["material_or_system"],
                        "property_name": prop,
                        "refno": row["refno"],
                        "paper_pdf_path": row.get("paper_pdf_path"),
                    }
                )
                n_placeholders += 1

    if n_placeholders:
        logger.info(
            f"Inserted {n_placeholders} placeholder rows for missing target properties"
        )

    # Renumber ids to be ascending within each refno
    result_df = pd.DataFrame(result_rows)
    if "id" in result_df.columns:
        counter_per_refno: dict[str, int] = {}
        new_ids: list[str] = []
        for refno in result_df["refno"]:
            refno_key = str(refno)
            idx = counter_per_refno.get(refno_key, 0)
            new_ids.append(f"prop_{idx:03d}")
            counter_per_refno[refno_key] = idx + 1
        result_df["id"] = new_ids

    return result_df


def split_by_annotators(
    df: pd.DataFrame, annotators: list[str], overlap: int, seed: int = 1
) -> dict[str, pd.DataFrame]:
    """Split a dataframe by refno across annotators with a shared overlap set.

    Args:
        df: DataFrame with a 'refno' column
        annotators: List of annotator names
        overlap: Number of refnos shared across all annotators
        seed: Random seed for shuffling refnos

    Returns:
        Dict mapping annotator name to their sub-dataframe

    """
    refnos = list(df["refno"].unique())
    random.seed(seed)
    random.shuffle(refnos)

    if overlap > len(refnos):
        logger.warning(
            f"Overlap ({overlap}) exceeds total refnos ({len(refnos)}), "
            "all refnos will be shared"
        )
        overlap = len(refnos)

    shared_refnos = refnos[:overlap]
    remaining_refnos = refnos[overlap:]

    # Round-robin assignment of remaining refnos
    annotator_unique: dict[str, list[str]] = {name: [] for name in annotators}
    for i, refno in enumerate(remaining_refnos):
        annotator_unique[annotators[i % len(annotators)]].append(refno)

    # Build sub-dataframes: overlap first, then unique
    result: dict[str, pd.DataFrame] = {}
    for name in annotators:
        assigned_refnos = shared_refnos + annotator_unique[name]
        sub_df = df[df["refno"].isin(set(assigned_refnos))].copy()
        # Sort so shared refnos come first
        shared_set = set(shared_refnos)
        sub_df["_is_shared"] = sub_df["refno"].isin(shared_set)
        sub_df = sub_df.sort_values("_is_shared", ascending=False, kind="mergesort")
        sub_df = sub_df.drop(columns=["_is_shared"]).reset_index(drop=True)
        result[name] = sub_df

        logger.info(
            f"Annotator '{name}': {len(assigned_refnos)} refnos "
            f"({overlap} shared + {len(annotator_unique[name])} unique)"
        )

    logger.info(f"Shared refnos ({overlap}): {shared_refnos}")

    return result


def main() -> None:
    """CLI entry point."""
    parser = argparse.ArgumentParser(
        description="Filter and classify property candidates from extracted properties CSV files"
    )

    # Add base pbench arguments
    parser = pbench.add_base_args(parser)
    parser.add_argument(
        "--target_properties",
        "-tp",
        nargs="+",
        type=str,
        default=None,
        help="List of target property names. Blank rows will be inserted for missing properties per material.",
    )
    parser.add_argument(
        "--annotators",
        "-a",
        nargs="+",
        type=str,
        default=None,
        help="List of annotator names to split the combined CSV among.",
    )
    parser.add_argument(
        "--overlap",
        type=int,
        default=10,
        help="Number of refnos shared across all annotators (default: 10).",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=1,
        help="Random seed for shuffling refnos (default: 1).",
    )

    args = parser.parse_args()

    # Setup logging using pbench's setup_logging
    pbench.setup_logging(args.log_level)

    # Define input and output directories
    if False:
        input_dir = args.output_dir / "unsupervised_llm_extraction"
    else:
        input_dir = args.output_dir / "preds"
    output_dir = args.output_dir / "candidates"

    # Check if input directory exists
    if not input_dir.exists():
        logger.error(f"Input directory does not exist: {input_dir}")
        logger.error(f"Expected to find CSV files in: {input_dir}")
        return

    # Find all CSV files in input directory
    csv_files = sorted(input_dir.glob("extracted_properties*.csv"))

    if not csv_files:
        logger.warning(
            f"No CSV files matching pattern 'extracted_properties*.csv' found in {input_dir}"
        )
        return

    logger.info(f"Found {len(csv_files)} CSV files to process")
    logger.info(f"Input directory: {input_dir}")
    logger.info(f"Output directory: {output_dir}")

    # Create output directory if it doesn't exist
    output_dir.mkdir(parents=True, exist_ok=True)

    # Process each CSV file and collect dataframes
    all_dfs = []
    for csv_file in csv_files:
        df = process_csv_file(csv_file)
        if df is not None:
            all_dfs.append(df)

    if not all_dfs:
        logger.error("No CSV files were successfully processed")
        return

    # Concatenate all dataframes
    combined_df = pd.concat(all_dfs, ignore_index=True)

    # Insert placeholder rows for missing target properties
    if args.target_properties:
        combined_df = insert_placeholder_rows(combined_df, args.target_properties)

    # Save to single CSV file
    output_file = output_dir / "extracted_properties_combined.csv"
    combined_df.to_csv(output_file, index=False)

    logger.info(f"Combined {len(all_dfs)} CSV files into {output_file}")
    logger.info(f"Total rows: {len(combined_df)}")

    # Log overall statistics
    if "data_type" in combined_df.columns:
        type_counts = combined_df["data_type"].value_counts()
        logger.info(f"Overall data type distribution: {type_counts.to_dict()}")

    # Split by annotators if requested
    if args.annotators:
        annotator_dfs = split_by_annotators(
            combined_df, args.annotators, args.overlap, args.seed
        )
        for name, sub_df in annotator_dfs.items():
            annotator_dir = (
                args.output_dir.parent / f"{args.output_dir.name}-{name}" / "candidates"
            )
            annotator_dir.mkdir(parents=True, exist_ok=True)
            out_path = annotator_dir / "extracted_properties_combined.csv"
            sub_df.to_csv(out_path, index=False)
            logger.info(f"Saved {len(sub_df)} rows to {out_path}")


if __name__ == "__main__":
    main()
